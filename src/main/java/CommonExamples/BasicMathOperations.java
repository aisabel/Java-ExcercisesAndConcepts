package CommonExamples;

public class BasicMathOperations {

    public static void main (String[] args) {
        //normal operations between two numbers with the result of the previous outcome
        int a = 2;
        int b = 3;
        int sum = a + b; //Adds two numbers
        System.out.println("Sum value:" + sum); //Print sum result
        int rest = sum - 3; //Adds two numbers
        System.out.println("Rest value:" + rest); //Print rest result
        int multi = rest *5;
        System.out.println("Mult value:" + multi); //Print multiply result
        int div = multi/6;
        System.out.println("Divide value:"+ div); //Print divide result

        //Pending to add operators

    }
}
